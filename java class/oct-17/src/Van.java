
public class Van {
	Van()
	{
		System.out.println("this is default constructor");
	}
	
	
	Van(int a)
	{
		System.out.println("this is int constructor");
	}
	
	Van(double a)
	{
		System.out.println("this is double constructor");
		
	}
	
	Van(int a, int b)
	{
		System.out.println("this is int,int constructor");
	}

}
